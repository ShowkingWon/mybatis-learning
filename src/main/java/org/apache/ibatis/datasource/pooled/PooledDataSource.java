/**
 * This is a simple, synchronous, thread-safe database connection pool.
 *
 * 有连接池的数据源 ,这个是mybatis自己实行了一个连接池.
 *
 * 实际场景下，我们基本不用 MyBatis 自带的数据库连接池的实现!!!!!!!
 *
 *
 * POOLED– 这种数据源的实现利用“池”的概念将 JDBC 连接对象组织起来，避免了创建新的连接实例时所必需的初始化和认证时间。
 * 这是一种使得并发 Web 应用快速响应请求的流行处理方式。
 *
 * 除了UNPOOLED 下的属性外，还有更多属性用来配置 POOLED 的数据源：
 *    poolMaximumActiveConnections – 在任意时间可以存在的活动（也就是正在使用）连接数量，默认值：10
 *    poolMaximumIdleConnections – 任意时间可能存在的空闲连接数,默认值：5
 *    poolMaximumCheckoutTime – 在被强制返回之前，池中连接被检出（checked out）时间，默认值：20000 毫秒（即 20 秒）
 *    poolTimeToWait – 这是一个底层设置，如果获取连接花费了相当长的时间，连接池会打印状态日志并重新尝试获取一个连接（避免在误配置的情况下一直安静的失败），默认值：20000 毫秒（即 20 秒）。
 *    poolMaximumLocalBadConnectionTolerance – 这是一个关于坏连接容忍度的底层设置， 作用于每一个尝试从缓存池获取连接的线程. 如果这个线程获取到的是一个坏的连接，那么这个数据源允许这个线程尝试重新获取一个新的连接，但是这个重新尝试的次数不应该超过 poolMaximumIdleConnections 与 poolMaximumLocalBadConnectionTolerance 之和。 默认值：3 (新增于 3.4.5)
 *    poolPingQuery – 发送到数据库的侦测查询，用来检验连接是否正常工作并准备接受请求。默认是“NO PING QUERY SET”，这会导致多数数据库驱动失败时带有一个恰当的错误消息。
 *    poolPingEnabled – 是否启用侦测查询。若开启，需要设置 poolPingQuery 属性为一个可执行的 SQL 语句（最好是一个速度非常快的 SQL 语句），默认值：false。
 *    poolPingConnectionsNotUsedFor – 配置 poolPingQuery 的频率。可以被设置为和数据库连接超时时间一样，来避免不必要的侦测，默认值：0（即所有连接每一时刻都被侦测 — 当然仅当 poolPingEnabled 为 true 时适用）。
 *
 *
 * 其他还有诸如C3P0的com.mchange.v2.c3p0.ComboPooledDataSource
 * DBCP 等等
 * 还有一些人喜欢自己用apache commons pool写一个连接池.
 *
 * @author Clinton Begin
 */
